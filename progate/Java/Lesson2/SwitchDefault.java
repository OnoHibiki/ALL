class SwitchDefault{
    public static void main(String[]args){
        int n = 0 ;


        switch(n){
            case 1 :
            System.out.println("大吉です");
            break ;

            case 2 :
            System.out.println("中吉です");

            default :
            System.out.println("凶です");
            break ;
        }
    }
}